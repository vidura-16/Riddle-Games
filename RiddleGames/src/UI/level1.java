    private void labelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelMouseClicked
      if(lblnextquestion.getText().equals("Restart the quiz"))
      {
      lblnextquestion.setText("NEXT");
             jPanel1.setBackground(new java.awt.Color(204, 204, 204));
             index=0;
              correct=0;
      }
         if(index == questions.length){
             lbl_question.setText("Your Score" + correct + " / " + questions.length);
             checkpie.setText("Check Score With pieChart");
              correctA= correct*10;
                
              setScore(correctA);
                 if(correct >=(float)questions.length/2)
                     {
                        jPanel1.setBackground(Color.green);
                 }else{
                
                jPanel1.setBackground(Color.CYAN);
               
                
         }
         lblnextquestion.setText("Restart the quiz");
       }else{
       
            enableRbuttons(true);
            lbl_question.setText(questions[index]); 
            lbllevel.setText(level[index]);
            jRadioButton1.setText(options[index][0]);
            jRadioButton2.setText(options[index][1]);
            jRadioButton3.setText(options[index][2]);
            jRadioButton4.setText(options[index][3]);
             if(index == questions.length-1){
                lblnextquestion.setText("finish and see the results");
            }
         
        }bg.clearSelection();
         
    }//GEN-LAST:event_labelMouseClicked

